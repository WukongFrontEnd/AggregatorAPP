import express from "express" ;
import renderer from "../controllers/example/renderer" ;
import apiProvider from "../controllers/example/apiProvider" ;
import cors from "../../system/middlewares/api/cors" ;
let router = express.Router() ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
这里先示范一个renderer性质的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/", function(req, res, next) {   
    new renderer(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
这里示范一个api性质的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/process", cors , function(req, res, next) {
    new apiProvider(req, res, next) ;
}) ;

export default router ;
