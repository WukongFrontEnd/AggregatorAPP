import AppApiControllerBasic from "../api" ;
import apiDataFilter from "../../../system/libraries/apiDataFilter" ;
import benchmark from "../../../system/libraries/benchmark" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个渲染器实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class RestfulApi extends AppApiControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        //this.outputs() ;
        this.invokeApi() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    输出数据
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    outputs() {
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        扩展模板数据
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
        Object.assign(this.jsonObject, { "status" : "200" , "message" : "终于搞成功了！" }) ;
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        输出内容
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/    
        this.output() ;         
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    进行api数据调用范例
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    async invokeApi() {
        let bm = new benchmark ;
        bm.mark("start") ;
        let adf = new apiDataFilter(this.req) ;
        let result = await adf.request({
            apiPath : "example.rent.detail" ,
            method : "get" ,
            converter : {
                mapper : "example" ,
                method : "detail"
            }
        }) ;
        bm.mark("end") ; 
        console.log(bm.markers) ;
        console.log("总耗时：" + bm.elapsedTime({ startMarker : "start" , endMarker : "end" }) + " s") ;
        this.res.send(JSON.stringify(result)) ;
    }
}
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
最后将render暴露出去
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
export default RestfulApi ;